console.log(process.version); // 실행된 노드 버전
console.log(process.arch); // 아키텍처 (x86, arm 등)
console.log(process.platform); // 운영체제 플랫폼 정보
console.log(process.pid); // Process ID (PID)
console.log(process.execPath); // node.exe 경로
console.log(process.cwd()); // 현재 이 파일이 실행된 경로 (디렉토리 까지)
console.log(process.cpuUsage()); // CPU 사용량
